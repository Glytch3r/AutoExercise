function AutoExercise.openPanel()
	local modal = ISAutoFitnessUI:new(0,0, 600, 350, getPlayer());
	modal:initialise()
	modal:addToUIManager()
end


function AutoExercise.context(player, context, worldobjects, test)
	local pl = getSpecificPlayer(player)
	local sq = clickedSquare
	pl:getFitness():init()

	if sq then
		local op = context:addOption('Auto Exercise Panel', worldobjects, function()
			AutoExercise.openPanel()
			context:hideAndChildren()
		end)
        op.iconTexture = getTexture("media/ui/AutoFitnessUI.png")
	end
end
Events.OnFillWorldObjectContextMenu.Remove(AutoExercise.context)
Events.OnFillWorldObjectContextMenu.Add(AutoExercise.context)

